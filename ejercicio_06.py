# Calcular la media de tres números pedidos por teclado

print('Calcular la media de la suma de tres números')

num_1 = int(input(" Ingresa el número: "))
num_2 = int(input(" Ingresa el número: "))
num_3 = int(input(" Ingresa el número; "))

media = ( num_1 + num_2 + num_3 ) / 3
print(" La media es ", media)
